"""[DE] Deterministic synthetic Track-C corpus (§33.3, D5).

The corpus is generated by construction, so its ground truth is known. Two things
must hold: (1) the same seed reproduces the same corpus byte-for-byte, and (2) the
production absence layer, run WITHOUT the value gate, confuses TRUE_MISS (value
stated in prose) with FALSE_MISS (property only named) — both collapse to
``possible_miss`` — while WITH the gate they separate correctly. That confusion is
the weakness the whole benchmark exists to measure.
"""

from __future__ import annotations

import collections
import tempfile
from pathlib import Path

from kg_eval.datasets.synthetic import build_synthetic
from kg_retrievers.absence_signals import classify_cell
from kg_retrievers.graph_store import KuzuGraphStore


def _store() -> KuzuGraphStore:
    return KuzuGraphStore(str(Path(tempfile.mkdtemp()) / "g"))


def test_manifest_is_deterministic() -> None:
    s1, s2 = _store(), _store()
    try:
        m1 = build_synthetic(s1)
        m2 = build_synthetic(s2)
        assert m1.to_dict() == m2.to_dict()  # same seed → identical corpus
    finally:
        s1.close()
        s2.close()


def test_label_histogram_and_gold_counts() -> None:
    s = _store()
    try:
        m = build_synthetic(s)
        assert len(m.cells) == 48  # 12 materials × 4 properties
        assert m.label_histogram() == {
            "present": 14,
            "genuine_gap": 20,
            "possible_miss": 12,
            "retracted": 2,
        }
        # gold facts: PRESENT_TABLE (8) + PRESENT_CATALOG (6) + TRUE_MISS (12) = 26,
        # of which the 12 TRUE_MISS are prose-only (not extractable offline).
        assert len(m.extraction_gold) == 26
        offline = [g for g in m.extraction_gold if not g.extractable_offline]
        assert len(offline) == 12 and all(g.modality == "chunk" for g in offline)
    finally:
        s.close()


def _verdicts_by_archetype(store: KuzuGraphStore, manifest, *, value_gate: bool) -> dict:
    by: dict[str, collections.Counter] = collections.defaultdict(collections.Counter)
    for c in manifest.cells:
        v = classify_cell(store, c.material_id, c.property_id, value_gate=value_gate).verdict
        by[c.archetype][v] += 1
    return by


def test_gate_off_confuses_true_and_false_miss() -> None:
    # The reproducible weakness: without the value signal, TRUE_MISS and FALSE_MISS
    # (and ABSENT) all read as possible_miss.
    s = _store()
    try:
        m = build_synthetic(s)
        by = _verdicts_by_archetype(s, m, value_gate=False)
        assert by["TRUE_MISS"] == {"possible_miss": 12}
        assert by["FALSE_MISS"] == {"possible_miss": 10}  # WRONG (reality genuine_gap)
        assert by["ABSENT"] == {"possible_miss": 10}  # WRONG (reality genuine_gap)
        assert by["PRESENT_TABLE"] == {"present": 8}
        assert by["PRESENT_CATALOG"] == {"present": 6}
        assert by["RETRACTED"] == {"retracted": 2}
    finally:
        s.close()


def test_gate_on_separates_true_and_false_miss() -> None:
    # With the value gate the discriminator resolves: TRUE_MISS keeps possible_miss,
    # FALSE_MISS and ABSENT become genuine_gap — every archetype now correct.
    s = _store()
    try:
        m = build_synthetic(s)
        by = _verdicts_by_archetype(s, m, value_gate=True)
        assert by["TRUE_MISS"] == {"possible_miss": 12}
        assert by["FALSE_MISS"] == {"genuine_gap": 10}
        assert by["ABSENT"] == {"genuine_gap": 10}
        assert by["PRESENT_TABLE"] == {"present": 8}
        assert by["PRESENT_CATALOG"] == {"present": 6}
        assert by["RETRACTED"] == {"retracted": 2}
    finally:
        s.close()


def test_scales_with_n_materials() -> None:
    s = _store()
    try:
        m = build_synthetic(s, n_materials=6)
        assert len(m.cells) == 24
        assert {c.true_label for c in m.cells} <= {
            "present",
            "genuine_gap",
            "possible_miss",
            "retracted",
        }
    finally:
        s.close()


def test_zero_materials_is_empty() -> None:
    s = _store()
    try:
        m = build_synthetic(s, n_materials=0)
        assert m.cells == [] and m.materials == []
        assert m.label_histogram() == dict.fromkeys(
            ("present", "genuine_gap", "possible_miss", "retracted"), 0
        )
    finally:
        s.close()
